package com.dashda.exception;

/**
 * @author mohamed.hanfy
 *
 */
public class ReportCallsDoneServiceException extends AppExceptionHandler {

	public ReportCallsDoneServiceException() {
		super();
		
	}

	public ReportCallsDoneServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ReportCallsDoneServiceException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ReportCallsDoneServiceException(String message) {
		super(message);
		
	}

	public ReportCallsDoneServiceException(Throwable cause) {
		super(cause);
		
	}

}
