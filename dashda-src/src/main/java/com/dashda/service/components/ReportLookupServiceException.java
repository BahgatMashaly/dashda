package com.dashda.service.components;

/**
 * @author mohamed.hanfy
 *
 */
public class ReportLookupServiceException extends Exception {

	/**
	 * 
	 */
	public ReportLookupServiceException() {
		
	}

	/**
	 * @param arg0
	 */
	public ReportLookupServiceException(String arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 */
	public ReportLookupServiceException(Throwable arg0) {
		super(arg0);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public ReportLookupServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	/**
	 * @param arg0
	 * @param arg1
	 * @param arg2
	 * @param arg3
	 */
	public ReportLookupServiceException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
